package com.kaka.math;

/**
 * 
 * Define a point in two dimensions space and its position in integer values
 * 
 * @author chinhdd.bk
 * 
 */
public class Point2D {

    public Point2D() {
        x = 0;
        y = 0;
    }

    public Point2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    private int x;
    private int y;
}
